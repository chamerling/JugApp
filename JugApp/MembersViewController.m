- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleDefault reuseIdentifier:CellIdentifier] autorelease];
        cell.accessoryType = UITableViewCellAccessoryDisclosureIndicator;
    }
    
    NSUInteger selected = [indexPath row];
    Member *selectedMember = [self.members objectAtIndex:selected];
    cell.textLabel.text = selectedMember.name;
    return cell;
}

#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSUInteger selected = indexPath.row;
    Member *selectedMember = [members objectAtIndex:selected];
    MemberDetailsViewController *memberViewController = [[MemberDetailsViewController alloc] init];
    memberViewController.member = selectedMember;
    memberViewController.title = @"Member Details";
    
    [self.navigationController pushViewController:memberViewController
										 animated:YES];
    [memberViewController release];
}

- (void)dealloc {
    [members release];
    [super dealloc];
}

